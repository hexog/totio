using Microsoft.AspNetCore.Components;
using Microsoft.AspNetCore.Components.Web;
using Totio.Client;
using Totio.Client.Data;
using App = Totio.Core.App;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
builder.Services.AddRazorPages();
builder.Services.AddServerSideBlazor();
builder.Services.AddSingleton<WeatherForecastService>();
builder.Services.AddHttpClient();

App.ConfigureServices(builder.Services);
builder.WebHost.ConfigureAppConfiguration(App.ConfigureAppConfiguration);

builder.Services.AddTotioAuthenticationClients();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (!app.Environment.IsDevelopment())
{
	app.UseExceptionHandler("/Error");
	// The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
	app.UseHsts();
}

app.UseHttpsRedirection();

app.UseStaticFiles();

app.UseRouting();

app.MapBlazorHub();
app.MapFallbackToPage("/_Host");

app.Run();
